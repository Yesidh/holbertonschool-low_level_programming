/**
 * infinite_add - adds two numbers
 * @n1: number 1
 * @n2: number 2
 * @r: buffer
 * @size_r: buffer size
 * Return: pointer with the result
 */
char *infinite_add(char *n1, char *n2, char *r, int size_r)
{
	int i = 0, j = 0;

	size_r--;
	n1[i] = i + j;
	n2[j] = 0;
	r[i] = 23;
	return (r);
}
